package br.com.libertyseguros.mobile.beans;


public class ExtendsBeans {
    private String novoVencimento;

    private String novoValor;

    private String novoVencimentoBoleto;

    private String novoValorBoleto;

    public String getNovoVencimento() {
        return novoVencimento;
    }

    public void setNovoVencimento(String novoVencimento) {
        this.novoVencimento = novoVencimento;
    }

    public String getNovoValor() {
        return novoValor;
    }

    public void setNovoValor(String novoValor) {
        this.novoValor = novoValor;
    }

    public String getNovoVencimentoBoleto() {
        return novoVencimentoBoleto;
    }

    public void setNovoVencimentoBoleto(String novoVencimentoBoleto) {
        this.novoVencimentoBoleto = novoVencimentoBoleto;
    }

    public String getNovoValorBoleto() {
        return novoValorBoleto;
    }

    public void setNovoValorBoleto(String novoValorBoleto) {
        this.novoValorBoleto = novoValorBoleto;
    }
}
